/**
*   短时间显示, 几分钟前,几秒前...
**/
function put_time($time = 0){
    $ttime = time() - $time;
    if($ttime <= 0 || $ttime < 60){
        return '几秒前';
    }   
    if($ttime > 60 && $ttime <120){
        return '1分钟前';
    }
    
    $i = floor($ttime / 60);                                //分
    $h = floor($ttime / 60 / 24);                       //时
    $d = floor($ttime / 86400);                         //天
    $m = floor($ttime / 2592000);                       //月
    $y = floor($ttime / 60 / 60 / 24 / 365);                //年
    if($i < 30){
        return $i.'分钟前';
    }
    if($i > 30 && $i < 60){
        return '一小时内';
    }
    if($h && $h < 24){
        return $h.'小时前';
    }
    if($d && $d < 30){
        return $d.'天前';
    }   
    if($m && $m < 12){      
        return $m.'个月前';
    }
    if($y){
        return $y.'年前';
    }   
    return date('Y-m-d H:i:s');
}

